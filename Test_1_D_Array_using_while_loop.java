package java_Array;
// printing all element of array using while loop.
public class Test_1_D_Array_using_while_loop 
{
	public static void main(String args[])
	{
		int[] a= {10,20,30};
		int b=0;
		while(b<a.length)
		{
			System.out.print(a[b]+" ");
			b++;
		}
		//for(int i=0;i<a.length;i++)
	}
	

}
